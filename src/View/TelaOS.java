    private void BtAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtAlterarActionPerformed
         if(TxId.getText().isEmpty())
           {
               JOptionPane.showMessageDialog(null, "Digite o Cpf que será alterado");
           }
           
           else
           {
               Os = new OS ();
               Os.setId(Integer.parseInt(TxId.getText()));
               Os.setIdCarro(Integer.parseInt(TxIdCarro.getText()));
               Os.setKm(TxKm.getText());
               Os.setMecanico(TxMecanico.getText());
               Os.setQuantPecas(TxQuantPecas.getText());
               Os.setServico(TxServico.getText());
               Os.setTipoServico(ComboServico.getSelectedItem().toString());
               Os.setValorPeca(TxValorPeca.getText());
               Os.setValorTotal(TxValorTotal.getText());
               BtSalvar.setEnabled(false);
               BtAlterar.setEnabled(false);
               JOptionPane.showMessageDialog(null, "Atualizado com sucesso!");
           }
    }//GEN-LAST:event_BtAlterarActionPerformed


